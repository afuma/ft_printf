int ft_putnbr_len(int n)
{
    int len;

    len = 0;
    if (n == INT_MIN) // -2147483648
        return (11);
    if (n == 0)
        return (1);
    if (n < 0)
    {
        len++;
        n *= -1;
    }
    while (n != 0)
    {
        len++;
        n /= 10;
    }
    return (len);
}